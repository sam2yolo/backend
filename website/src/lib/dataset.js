// Compile SAM 3.1 results into a YOLO-compatible dataset (zip).
//
// Output layout:
//   <name>/
//     data.yaml
//     images/train/frame_XXXXXX.jpg
//     labels/train/frame_XXXXXX.txt
//     images/val/frame_XXXXXX.jpg
//     labels/val/frame_XXXXXX.txt
//
// Boxes come straight from SAM. Each text prompt is one class (its index in the
// prompt list = class id). Segmentation-polygon labels (YOLO-seg) are supported
// via `format: 'segment'` and yoloSegLabelText().
import JSZip from 'jszip';

export function yoloLabelText(boxes, width, height) {
	const lines = [];
	if (!(width > 0 && height > 0)) return '';
	for (const b of boxes) {
		const x1 = Math.min(Math.max(Number(b.x1), 0), width);
		const y1 = Math.min(Math.max(Number(b.y1), 0), height);
		const x2 = Math.min(Math.max(Number(b.x2), 0), width);
		const y2 = Math.min(Math.max(Number(b.y2), 0), height);
		const cx = (x1 + x2) / 2 / width;
		const cy = (y1 + y2) / 2 / height;
		const w = Math.abs(x2 - x1) / width;
		const h = Math.abs(y2 - y1) / height;
		if (!(w > 0 && h > 0)) continue;
		lines.push(`${b.classId} ${cx.toFixed(6)} ${cy.toFixed(6)} ${w.toFixed(6)} ${h.toFixed(6)}`);
	}
	return lines.join('\n');
}

const clamp01 = (v) => (v < 0 ? 0 : v > 1 ? 1 : v);

/**
 * YOLO segmentation labels: one line per instance `cls x1 y1 x2 y2 ...` with
 * normalized, clamped [0,1] coords. Skips polygons with fewer than 3 points.
 * @param {Array<{classId:number, points:[number,number][]}>} polys
 */
export function yoloSegLabelText(polys, width, height) {
	const lines = [];
	for (const p of polys) {
		const pts = p.points || [];
		if (pts.length < 3) continue;
		if (!(width > 0 && height > 0)) continue;
		const coords = [];
		for (const [x, y] of pts) {
			coords.push(clamp01(x / width).toFixed(6), clamp01(y / height).toFixed(6));
		}
		lines.push(`${p.classId} ${coords.join(' ')}`);
	}
	return lines.join('\n');
}

function dataYaml(classes, { hasVal = true } = {}) {
	const names = classes.map((c, i) => `  ${i}: ${JSON.stringify(String(c))}`).join('\n');
	return `# Auto-generated by Fleet Vision Studio (SAM 3.1 → YOLO)
path: .
train: images/train
val: ${hasVal ? 'images/val' : 'images/train'}
nc: ${classes.length}
names:
${names}
`;
}

function splitForIndex(index, frameCount, valRatio) {
	if (frameCount < 5 || !(valRatio > 0)) return 'train';
	const valCount = Math.max(1, Math.round(frameCount * valRatio));
	const stride = Math.max(2, Math.floor(frameCount / valCount));
	return (index + 1) % stride === 0 ? 'val' : 'train';
}

/**
 * Build a YOLO dataset zip. For `format: 'detect'` (default) each frame supplies
 * `boxes`; for `format: 'segment'` each frame supplies `polygons`.
 * @param {{ classes:string[], frames:Array<object>, name?:string, format?:'detect'|'segment', valRatio?:number }} opts
 * @returns {Promise<{blob:Blob, frameCount:number, labeledFrames:number, boxesTotal:number, skippedFrames:number, format:string, trainFrames:number, valFrames:number}>}
 */
export async function buildYoloDataset({
	classes,
	frames,
	name = 'sam-dataset',
	format = 'detect',
	valRatio = 0.2
}) {
	const usableFrames = frames.filter((frame) => frame.imageBlob);
	const zip = new JSZip();
	const root = zip.folder(name);
	const hasVal = usableFrames.length >= 5 && valRatio > 0;
	root.file('data.yaml', dataYaml(classes, { hasVal }));
	root.file(
		'README.md',
		`# ${name}

YOLO dataset generated from SAM inference results.

- Format: ${format === 'segment' ? 'YOLO segmentation' : 'YOLO detection'}
- Classes: ${classes.length}
- Frames: ${usableFrames.length}
`
	);
	const imageDirs = {
		train: root.folder('images/train'),
		val: root.folder('images/val')
	};
	const labelDirs = {
		train: root.folder('labels/train'),
		val: root.folder('labels/val')
	};

	const segment = format === 'segment';
	let labeledFrames = 0;
	let boxesTotal = 0; // detect: box count; segment: instance (polygon) count
	let trainFrames = 0;
	let valFrames = 0;
	for (let index = 0; index < usableFrames.length; index += 1) {
		const frame = usableFrames[index];
		const split = splitForIndex(index, usableFrames.length, Number(valRatio));
		if (split === 'val') valFrames += 1;
		else trainFrames += 1;
		const imageExt = frame.imageExt || 'jpg';
		imageDirs[split].file(`${frame.name}.${imageExt}`, frame.imageBlob);
		if (segment) {
			const polys = frame.polygons || [];
			labelDirs[split].file(
				`${frame.name}.txt`,
				yoloSegLabelText(polys, frame.width, frame.height)
			);
			if (polys.length) labeledFrames += 1;
			boxesTotal += polys.length;
		} else {
			const boxes = frame.boxes || [];
			labelDirs[split].file(`${frame.name}.txt`, yoloLabelText(boxes, frame.width, frame.height));
			if (boxes.length) labeledFrames += 1;
			boxesTotal += boxes.length;
		}
	}

	const blob = await zip.generateAsync({ type: 'blob' });
	return {
		blob,
		frameCount: usableFrames.length,
		labeledFrames,
		boxesTotal,
		skippedFrames: frames.length - usableFrames.length,
		format,
		trainFrames,
		valFrames
	};
}
